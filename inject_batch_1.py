def inject_chunk(logs_data):
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    
    for log in logs_data:
        cursor.execute('''
        INSERT INTO logs (log_id, timestamp, user_text, ai_text, status, pops)
        VALUES (?, ?, ?, ?, ?, ?)
        ''', (
            log["log"], 
            log["timestamp"], 
            log["user"], 
            log["ai"], 
            log["status"], 
            ",".join(log["pops"])
        ))
    
    conn.commit()
    conn.close()

